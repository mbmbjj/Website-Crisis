        </div>
        <div class="column-white" id='right-column'>
            <p class="infotxt">
                การแพ้อาหาร เป็นภาวะที่เกี่ยวกับการที่มีอาการแพ้หลังรับประทานอาหารนั้น
                โดยส่วนมากพบในเด็ก แต่ละช่วงอายุ ซึ่งเป็นภาวะที่พบได้มากขึ้น
                โดยการแพ้อาหาร ส่วนใหญ่จะดีขึ้นเมื่ออายุมากขึ้น แต่อาจไม่หาย
                หรือแพ้ตลอดชีวิตได้ในกลุ่มแพ้อาหารทะเลและถั่วลิสงหรือ ถั่วเปลือกแข็ง
                และหากแพ้รุนแรง(anaphylaxis)อาจทำให้มีอันตรายถึงแก่ชีวิตได้ <br />
                <br />
                โดยการแพ้อาหารแบ่งเป็น ดังนี้ <br />
                1.กลุ่มที่มีอาการเฉียบพลัน จะมีอาการภายใน 2-4 ชั่วโมงหลังกิน <br />
                2.กลุ่มที่มีอาการแบบล่าช้า หรือสะสม จะมีอาการหลังกินอาหารนี้ไปแล้วนาน
                2-3 วันถึงหลายสัปดาห์ <br />
                <br />
                โดยเด็กจะต้องหลีกเลี่ยงอาหารเหล่านั้นจนกว่าแพทย์จะประเมินอาการว่าดีขึ้น
                สามารถกินอาหารนั้นได้ ซึ่งสร้างความลำบากในการใช้ชีวิตของผู้ป่วย
                และการที่มีความรู้ ทราบอาหารที่แพ้หรืออ่านฉลากถูกต้อง
                ทราบถึงอาหารที่มีส่วนประกอบอาหารเสี่ยงนั้น
                จะช่วยให้คนไข้ปลอดภัยและใช้ชีวิตได้ดีขึ้น
            </p>

